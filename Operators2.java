public class Operators2 {
    public static void main(String[] args) {
        
        // 4.Logical Operators(&&, ||, !)

        int x = 3;
        System.out.println(x>3 && x>10);   // Logical AND
        System.out.println(x>3 || x>10);   // Logical OR
        System.out.println(!(x>3 && x>10)); // Logical NOT
        System.out.println(!(x>3 || x>10));   // Logical NOT

        //


    }
    
}
